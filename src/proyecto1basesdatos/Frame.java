    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String src = jTextArea1.getText();
        src+=" ";
        jTextArea2.setText("");
        this.error=false;
        try{
            SQLLexer lexer  = new SQLLexer(new ANTLRInputStream(src));
            //lexer.removeErrorListeners();
            //lexer.addErrorListener(new ThrowingErrorListener(this));        
            TokenStream tokenStream = new CommonTokenStream(lexer);
            
            SQLParser parser = new SQLParser(tokenStream);
            parser.removeErrorListeners();
            parser.addErrorListener(new ThrowingErrorListener(this));
            parser.query().inspect(parser);
            if (!error) {
                dbms.executeQuery(src);
                System.out.print("  ");
            }            
        }
        
        catch(Exception e){
            jTextArea2.setText(e.getMessage());
            e.printStackTrace();
                   
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    
    private class LineNumberModelImpl implements NumeroLineaModel{



		@Override

		public int getNumberLines() {

			return jTextArea1.getLineCount();

		}



		@Override

		public Rectangle getLineRect(int line) {

			try{

				return jTextArea1.modelToView(jTextArea1.getLineStartOffset(line));

			}catch(BadLocationException e){

				e.printStackTrace();

				return new Rectangle();

			}

		}

	}
